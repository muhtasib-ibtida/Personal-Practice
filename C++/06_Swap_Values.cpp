//Take two numbers as input, swap their values, and display the swapped values.
#include<iostream>
using namespace std;

int main(){

int a = 10;
    int b = 20;

    cout<<" a :"<<a<<endl;
    cout<<" b :"<<b<<endl;

    int c = a ;

    a = b ;
    b = c ;

    cout<<" a :"<<a<<endl;
    cout<<" b :"<<b<<endl;


    return 0;
}
